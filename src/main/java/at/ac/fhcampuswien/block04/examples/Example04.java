package at.ac.fhcampuswien.block04.examples;

public class Example04 {
    public static void main(String[] args) {
        /* for loops:
           - are identically to "while" loops!
           - a shorter version for counting loops */
        for (int i = 0; i < 5; i = i + 1) {
            System.out.println("*");
        }
    }
}