package at.ac.hcw.block06.examples.example03;

public class Cat {
    private int size;
    private String breed;
    private String name;

    public void meow() {
        System.out.println("Meow! Brrrrrr");
    }

    public void setSize(int size) {
        this.size = size;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }

    public void setName(String name) {
        this.name = name;
    }
}